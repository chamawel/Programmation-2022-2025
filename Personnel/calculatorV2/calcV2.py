class calc: 
    def __init__(self,x : float ,y : float ) -> None:
        self.x = x
        self.y = y
    
    def addition(x, y)      -> float:
        return x + y
    
    def substraction(x, y)  -> float:
        return x - y
    
    def multiplication(x, y) -> float:
        return x * y

    def divide(x, y) -> float:
        return x / y